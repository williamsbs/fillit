#include "fillit.h"

void	ft_putup(char *str)
{
	int	i;
	int	stock;

	i = 0;
	while (str[i] != '#')
		i++;
	stock = i;
	while (i < 20)
	{
		if (str[i] == '#')
		{
			str[i - ((stock / 5) * 5)] = '#';
			if ((stock / 5) != 0)
				str[i] = '.';
		}
		i++;
	}
}
